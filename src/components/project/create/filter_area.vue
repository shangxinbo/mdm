            })
        },
        computed: {
            allChecked() {
                let status = true
                let _this = this
                if (this.tag2.length > 0) {
                    this.tag2.forEach(el => {
                        if (_this.cart_pre.indexOf(el.code) < 0) {
                            status = false
                        }
                    })
                    return status
                } else {
